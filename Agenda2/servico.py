class Servico:
    def __init__(self):
        self.__id = ""
        self.__descricao = ""
        self.__valor = ""
    def set__id(self, id):
        id = self.__id
    def set__descricao(self, descricao):
        descricao = self.__descricao
    def set__valor(self, valor):
        valor = self.__valor
    def get__id(self):
        return self.__id
    def get__descricao(self):
        return self.__descricao
    def get__valor(self):
        return self.__valor